// Name is an exported string with methods to manipulate the data instead
// of using array/slice access modifiers
type Name string

// Lower converts the Name object c to lowercase
func (n Name) Lower() Name {
	return Name(common.StringToLower(string(n)))
}

// Upper converts the Name object c to uppercase
func (n Name) Upper() Name {
	return Name(common.StringToUpper(string(n)))
}

// String converts the Name object c to string
func (n Name) String() string {
	return string(n)
}

// UnmarshalJSON conforms the Unmarshaler interface
func (n *Name) UnmarshalJSON(res []byte) error {
	var s string
	err := json.Unmarshal(res, &s)
	if err != nil {
		return err
	}

	*n = Name(s)
	return nil
}

// Sign is an exported string signifying a currency sign
type Sign string

// String converts the Sign object s to string
func (s Sign) String() string {
	return string(s)
}

